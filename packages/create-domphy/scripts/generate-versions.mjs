// Regenerates src/versions.generated.ts from the current
// packages/{core,theme,ui}/package.json "version" fields. Run before every
// build/publish so the scaffolded starter's package.json pins each @domphy
// dependency to its own real published version, since the monorepo is no
// longer lockstep-versioned across packages (each bumps independently).
import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(HERE, "../../.."); // monorepo root

async function readVersion(packageName) {
  const packagePath = resolve(
    REPO_ROOT,
    "packages",
    packageName,
    "package.json",
  );
  const parsed = JSON.parse(await readFile(packagePath, "utf8"));
  if (!parsed.version) {
    throw new Error(
      `packages/${packageName}/package.json has no "version" field`,
    );
  }
  return parsed.version;
}

const [coreVersion, themeVersion, uiVersion] = await Promise.all([
  readVersion("core"),
  readVersion("theme"),
  readVersion("ui"),
]);

const output = `// AUTO-GENERATED by scripts/generate-versions.mjs — do not edit by hand.
// Regenerated on every build from packages/{core,theme,ui}/package.json.
export const CORE_VERSION = ${JSON.stringify(coreVersion)};
export const THEME_VERSION = ${JSON.stringify(themeVersion)};
export const UI_VERSION = ${JSON.stringify(uiVersion)};
`;

await writeFile(resolve(HERE, "../src/versions.generated.ts"), output, "utf8");
