// Generates apps/web/docs/demos/visual/blocks-catalog.ts from every demo under
// docs/demos/blocks/*.ts. Re-run via `pnpm --filter domphy-web visual:blocks-gen`
// (or `node scripts/gen-visual-blocks.mjs` from apps/web) after adding demos.
import { readdir, readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, ".."); // apps/web
const REPO = resolve(ROOT, "../..");
const DEMOS_DIR = resolve(ROOT, "docs/demos/blocks");
const OUT_DIR = resolve(ROOT, "docs/demos/visual");
const OUT_FILE = resolve(OUT_DIR, "blocks-catalog.ts");

/** @type {Array<{ exportName: string, category: string, filePath: string }>} */
const registry = JSON.parse(
  await readFile(resolve(REPO, "packages/blocks/registry.json"), "utf8"),
);

/** @type {Map<string, { category: string, filePath: string }>} */
const byName = new Map();
for (const entry of registry) {
  byName.set(entry.exportName, entry);
  byName.set(entry.exportName.toLowerCase(), entry);
}

/**
 * Layouts: full-page / wide blocks (sidebar*, dashboard*, Login*, signup*).
 * Charts: chart* recipes.
 * Effects: Magic UI effect / background / text animation categories.
 * Components: remaining Magic UI (core, community, device-mocks, buttons).
 * @param {string} name
 */
function sectionOf(name) {
  if (
    /^sidebar/i.test(name) ||
    /^dashboard/i.test(name) ||
    /^Login/i.test(name) ||
    /^signup/i.test(name)
  ) {
    return "Layouts";
  }
  if (/^chart/i.test(name)) return "Charts";
  const entry = byName.get(name) ?? byName.get(name.toLowerCase());
  const category = entry?.category;
  if (
    category === "effects" ||
    category === "backgrounds" ||
    category === "text"
  ) {
    return "Effects";
  }
  return "Components";
}

/** Valid JS identifier for the import binding. */
function importId(name) {
  return `demo_${name.replace(/[^a-zA-Z0-9_]/g, "_")}`;
}

const files = (await readdir(DEMOS_DIR))
  .filter((f) => f.endsWith(".ts"))
  .map((f) => f.slice(0, -3))
  .sort((a, b) => a.localeCompare(b));

/** @type {Record<string, string[]>} */
const sections = {
  Components: [],
  Effects: [],
  Charts: [],
  Layouts: [],
};

for (const name of files) {
  sections[sectionOf(name)].push(name);
}

const lines = [];
lines.push(
  "// AUTO-GENERATED by scripts/gen-visual-blocks.mjs — do not edit by hand.",
);
lines.push(
  '// Re-run: pnpm --filter domphy-web visual:blocks-gen  (or node scripts/gen-visual-blocks.mjs)',
);
lines.push(`import { visualCell, visualSection, visualPage } from "./cell.js";`);
lines.push("");

for (const name of files) {
  lines.push(
    `import ${importId(name)} from "../blocks/${name}.js";`,
  );
}
lines.push("");

function cellExpr(name) {
  const id = `block-${name}`;
  const section = sectionOf(name);
  // Clip large/sticky shells so cell screenshots do not bleed into neighbors.
  if (section === "Layouts") {
    return `visualCell(${JSON.stringify(id)}, ${JSON.stringify(name)}, ${importId(name)}, { minWidth: "360px", maxHeight: "420px", overflow: "hidden" })`;
  }
  if (section === "Charts") {
    return `visualCell(${JSON.stringify(id)}, ${JSON.stringify(name)}, ${importId(name)}, { minWidth: "320px", maxHeight: "360px", overflow: "hidden" })`;
  }
  if (section === "Effects") {
    return `visualCell(${JSON.stringify(id)}, ${JSON.stringify(name)}, ${importId(name)}, { minWidth: "240px", maxHeight: "280px", overflow: "hidden" })`;
  }
  return `visualCell(${JSON.stringify(id)}, ${JSON.stringify(name)}, ${importId(name)})`;
}

const sectionOrder = ["Components", "Effects", "Charts", "Layouts"];
const sectionExprs = [];
for (const title of sectionOrder) {
  const names = sections[title];
  if (names.length === 0) continue;
  const cells = names.map((n) => `    ${cellExpr(n)},`).join("\n");
  sectionExprs.push(
    `  visualSection(${JSON.stringify(title)}, [\n${cells}\n  ]),`,
  );
}

lines.push(`export default visualPage("Blocks", [`);
lines.push(sectionExprs.join("\n"));
lines.push(`]);`);
lines.push("");

await mkdir(OUT_DIR, { recursive: true });
await writeFile(OUT_FILE, lines.join("\n"), "utf8");

const counts = sectionOrder
  .map((t) => `${t}=${sections[t].length}`)
  .join(", ");
console.log(
  `Wrote ${OUT_FILE}\n  ${files.length} demos → ${counts}`,
);
